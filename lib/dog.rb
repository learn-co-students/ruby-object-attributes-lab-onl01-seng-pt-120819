class Dog 
  
  # setter method where the argument is set equal to the instance variable 
  def name=(dog_name)
    @name = dog_name
  end
  
  
  # getter method where the instance variable in the setter method is ?
  def name
    @name
  end 
  
  # setter method for breed 
  def breed=(dog_breed)
    @breed = dog_breed
  end 
  
  def breed 
    @breed 
  end 
  
end 

